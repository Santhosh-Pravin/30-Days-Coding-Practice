class Age {
    public static void main(String[] args) {
        System.out.println("Age of Harry");
        int birthyear_harry = 2000; // Birth year of Harry
        int currentyear = 2024; // Current year
        int age_harry = currentyear - birthyear_harry;

        System.out.println("Harry's age in the year 2024 is: " + age_harry);
    }
}
